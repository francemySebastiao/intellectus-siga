package ao.co.intellectus.DTO;

public class AlunoGuia {
	private Integer aluno;
	private Integer emolumento;
	private float emolumentoValor;
	private Integer desconto;
	private String turno;

	public Integer getAluno() {
		return aluno;
	}

	public void setAluno(Integer aluno) {
		this.aluno = aluno;
	}

	public Integer getEmolumento() {
		return emolumento;
	}

	public void setEmolumento(Integer emolumento) {
		this.emolumento = emolumento;
	}

	public float getEmolumentoValor() {
		return emolumentoValor;
	}

	public void setEmolumentoValor(float emolumentoValor) {
		this.emolumentoValor = emolumentoValor;
	}

	public Integer getDesconto() {
		return desconto;
	}

	public void setDesconto(Integer desconto) {
		this.desconto = desconto;
	}

	public String getTurno() {
		return turno;
	}

	public void setTurno(String turno) {
		this.turno = turno;
	}
	
	
}
