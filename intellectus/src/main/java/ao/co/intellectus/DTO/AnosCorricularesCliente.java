package ao.co.intellectus.DTO;

public class AnosCorricularesCliente {
     private int primeiro;
     private int segundo;
     private int terceiro;
     private int quarto;
     private int quinto;
     private Integer ultimoAno;
	public int getPrimeiro() {
		return primeiro;
	}
	public void setPrimeiro(int primeiro) {
		this.primeiro = primeiro;
	}
	public int getSegundo() {
		return segundo;
	}
	public void setSegundo(int segundo) {
		this.segundo = segundo;
	}
	public int getTerceiro() {
		return terceiro;
	}
	public void setTerceiro(int terceiro) {
		this.terceiro = terceiro;
	}
	public int getQuarto() {
		return quarto;
	}
	public void setQuarto(int quarto) {
		this.quarto = quarto;
	}
	public int getQuinto() {
		return quinto;
	}
	public void setQuinto(int quinto) {
		this.quinto = quinto;
	}
	public Integer getUltimoAno() {
		return ultimoAno;
	}
	public void setUltimoAno(Integer ultimoAno) {
		this.ultimoAno = ultimoAno;
	}
	
}
