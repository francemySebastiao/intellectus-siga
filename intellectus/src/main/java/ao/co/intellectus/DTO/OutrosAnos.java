package ao.co.intellectus.DTO;

public class OutrosAnos {
	private boolean anoCurricularValidado;
	private Integer anoCurricular;
	
	public boolean isAnoCurricularValidado() {
		return anoCurricularValidado;
	}
	public void setAnoCurricularValidado(boolean anoCurricularValidado) {
		this.anoCurricularValidado = anoCurricularValidado;
	}
	public Integer getAnoCurricular() {
		return anoCurricular;
	}
	public void setAnoCurricular(Integer anoCurricular) {
		this.anoCurricular = anoCurricular;
	}
}
