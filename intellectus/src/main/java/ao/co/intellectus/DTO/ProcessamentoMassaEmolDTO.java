package ao.co.intellectus.DTO;

public class ProcessamentoMassaEmolDTO {

	private Integer emolumentoId;
	private String nomeEmolumento;
	private Double valorEmolumento;
	
	
	public Integer getEmolumentoId() {
		return emolumentoId;
	}
	public void setEmolumentoId(Integer emolumentoId) {
		this.emolumentoId = emolumentoId;
	}
	public String getNomeEmolumento() {
		return nomeEmolumento;
	}
	public void setNomeEmolumento(String nomeEmolumento) {
		this.nomeEmolumento = nomeEmolumento;
	}
	public Double getValorEmolumento() {
		return valorEmolumento;
	}
	public void setValorEmolumento(Double valorEmolumento) {
		this.valorEmolumento = valorEmolumento;
	}
	
	
}
